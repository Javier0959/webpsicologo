<p>¿Alguna vez te has cuestionando quién eres? ¿O quizá cuál es tu propósito o cuáles son tus valores? Si es así, es posible que esté pasando por lo que algunos llaman una crisis de identidad.</p>
<p>El término «crisis de identidad» fue propuesto por primera vez del psicólogo y psicoanalista del desarrollo Erik Erikson. Quien introdujo la idea de las crisis de identidad de los adolescentes, así como las crisis de la mediana edad, creyendo que las personalidades se desarrollan al resolver las crisis en la vida.</p>
<p>Estas crisis a menudo ocurren cuando experimentamos grandes cambios o factores estresantes en la vida, o debido a factores como la edad o el avance de una determinada etapa (por ejemplo, la escuela, el trabajo o la infancia). Para conocer más sobre las crisis de identidad y como sobrevivir a ellas continúa leyendo.</p>


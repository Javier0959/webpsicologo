<h1>¿Qué es una crisis de identidad?</h1>
<p>Una crisis de identidad es un evento de desarrollo que involucra a una persona que cuestiona su sentido de sí mismo o su lugar en el mundo. El concepto se origina en el trabajo del psicólogo del desarrollo Erik Erikson, quien creía que la formación de la identidad era uno de los conflictos más importantes que enfrenta la gente.<br><br>

Según Erikson, una crisis de identidad es un momento de intenso análisis y exploración de diferentes formas de mirarse a uno mismo. Erikson señaló que desarrollar un sentido de identidad es importante durante los años de la adolescencia, aunque la formación y el crecimiento de la identidad no se limitan solo a la adolescencia.</p>

<h1>Síntomas de una crisis de identidad</h1>
<p>Una persona que atraviesa una crisis de identidad puede estar preocupada por ciertas preguntas:<br>

•●¿Qué me apasiona?<br>
•●¿Cuáles son mis creencias espirituales?<br>
•●¿Cuáles son mis valores?<br>
•●¿Cuál es mi papel en la sociedad o el propósito en la vida?<br>
•●¿Quién soy? (Esta pregunta puede ser en general o con respecto a las relaciones, la edad o la carrera).<br><br>

Si bien todos cuestionan su sentido de sí mismos de vez en cuando, es posible que tengas una crisis de identidad si estás pasando por un gran cambio o un momento estresante y las preguntas anteriores comienzan a interferir con su vida diaria. También puede notar que se siente más irritable, desmotivado o vacío.<br><br>

Es importante ser consciente de que tener sentimientos negativos sobre ti mismo o tu vida puede ser un indicador de vulnerabilidad a la depresión. Si también experimentas síntomas de depresión como mal humor, pérdida de interés, fatiga e irritabilidad, puedes encontrar un psicólogo que te acompañe en tu proceso.</p>

